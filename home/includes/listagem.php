<h2 style="text-align: center;">Bem vindo(a) ao ePAD</h2>

<p>Acompanhando os avanços tecnológicos para garantir a eficiência, transparência e precisão nas atividades desenvolvidas pelos docentes. Apresentamos o ePAD, um sistema intuitivo para o preenchimento do Plano de Atividades Docentes (PAD).</p>

<p>O ePAD surge como uma resposta às limitações dos métodos utilizados ate entao. Ao migrar para uma plataforma digital, o ePAD oferece uma série de vantagens que revolucionam a maneira como os docentes registram e gerenciam suas atividades:</p>
<ul>
  <li>Automatização do cálculo de horas: Com o ePAD, o cálculo de horas dedicadas a cada atividade torna-se automático, eliminando erros manuais e economizando tempo precioso </li>
  <li>Integração de dados: O sistema permite o cruzamento de informações, facilitando o preenchimento do PAD ao oferecer sugestões baseadas em dados já registrados, garantindo consistência e coesão nos relatórios.</li>
  <li>Segurança e veracidade dos dados: Ao centralizar todas as informações em uma base de dados segura, o ePAD reduz o risco de perda ou extravio de documentos, além de garantir a integridade e veracidade das informações registradas.</li>
</ul>

<p>Esperamos que o sistema possa auxilia-lo a fazer essa tarefa de maneira mais rapida</p>
